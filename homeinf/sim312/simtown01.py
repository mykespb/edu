# Параметры города
CITY_SIZE = 10  # Размер города (количество кварталов по одной стороне)
BLOCK_WIDTH = 1  # Ширина каждого квартала
STREET_WIDTH = 0.2  # Ширина улицы

# Расчет общей ширины города, включая улицы
TOTAL_CITY_DIM = CITY_SIZE * BLOCK_WIDTH + (CITY_SIZE + 1) * STREET_WIDTH

# Определение границ улиц и перекрестков
def get_street_coords(city_size, block_width, street_width):
    streets = []
    intersections = []

    # Горизонтальные и вертикальные улицы
    for i in range(city_size + 1):
        # Горизонтальные улицы
        y_start = i * (block_width + street_width)
        y_end = y_start + street_width
        streets.append({'type': 'horizontal', 'coords': (0, TOTAL_CITY_DIM, y_start, y_end)})

        # Вертикальные улицы
        x_start = i * (block_width + street_width)
        x_end = x_start + street_width
        streets.append({'type': 'vertical', 'coords': (x_start, x_end, 0, TOTAL_CITY_DIM)})

    # Определение перекрестков
    for i in range(city_size + 1):
        for j in range(city_size + 1):
            x_center = i * (block_width + street_width) + street_width / 2
            y_center = j * (block_width + street_width) + street_width / 2
            intersections.append((x_center, y_center))

    return streets, intersections

streets, intersections = get_street_coords(CITY_SIZE, BLOCK_WIDTH, STREET_WIDTH)

