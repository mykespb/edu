"""### 3. Логика движения людей"""

import math

# Добавляем в класс Person логику для движения по улицам и смены направления
class Person:
    def __init__(self, intersections, speed_range=(0.05, 0.15)):
        initial_intersection = random.choice(intersections)
        self.x, self.y = initial_intersection
        self.speed = random.uniform(*speed_range)

        # Направления: 0:право (x+), 1:верх (y+), 2:лево (x-), 3:низ (y-)
        self.direction = random.choice([0, 1, 2, 3])

        # Для отслеживания, приближается ли человек к перекрестку
        self.target_intersection = None

    def is_near_intersection(self, intersection_point, tolerance=0.1):
        return math.dist((self.x, self.y), intersection_point) < tolerance

    def get_current_street_segment(self, streets):
        # Определяет, на каком сегменте улицы находится человек
        # Упрощенная логика: предполагаем, что человек всегда на улице.
        # В реальной симуляции может потребоваться более сложная проверка.
        for street in streets:
            x1, x2, y1, y2 = street['coords']
            if street['type'] == 'horizontal':
                # Проверяем, находится ли y-координата человека внутри горизонтальной улицы
                # и x-координата в пределах города
                if y1 - 0.01 <= self.y <= y2 + 0.01 and 0 <= self.x <= TOTAL_CITY_DIM:
                    return street
            elif street['type'] == 'vertical':
                # Проверяем, находится ли x-координата человека внутри вертикальной улицы
                # и y-координата в пределах города
                if x1 - 0.01 <= self.x <= x2 + 0.01 and 0 <= self.y <= TOTAL_CITY_DIM:
                    return street
        return None

    def update_direction(self, intersections, streets):
        # Проверяем, находится ли человек на перекрестке
        on_intersection = False
        current_intersection = None
        for intersection in intersections:
            if self.is_near_intersection(intersection, tolerance=self.speed/2):
                on_intersection = True
                current_intersection = intersection
                break

        if on_intersection:
            # Выравниваем позицию по перекрестку для точности
            self.x, self.y = current_intersection

            # Возможные новые направления
            possible_directions = []

            # Проверяем, куда можно двигаться с этого перекрестка
            # Для простоты, если есть улица в этом направлении, то это возможное направление
            # 0:право (x+), 1:верх (y+), 2:лево (x-), 3:низ (y-)

            # Проверяем горизонтальные улицы
            # Ищем горизонтальную улицу, которая проходит через текущий перекресток
            for street in streets:
                if street['type'] == 'horizontal':
                    x_start, x_end, y_start, y_end = street['coords']
                    if y_start <= current_intersection[1] <= y_end:
                        # Если перекресток находится на горизонтальной улице
                        # Можно двигаться вправо или влево
                        if self.direction != 2: # Не разворачиваемся на 180 градусов сразу
                            possible_directions.append(0)
                        if self.direction != 0: # Не разворачиваемся на 180 градусов сразу
                            possible_directions.append(2)
                        break

            # Проверяем вертикальные улицы
            for street in streets:
                if street['type'] == 'vertical':
                    x_start, x_end, y_start, y_end = street['coords']
                    if x_start <= current_intersection[0] <= x_end:
                        # Если перекресток находится на вертикальной улице
                        # Можно двигаться вверх или вниз
                        if self.direction != 3: # Не разворачиваемся на 180 градусов сразу
                            possible_directions.append(1)
                        if self.direction != 1: # Не разворачиваемся на 180 градусов сразу
                            possible_directions.append(3)
                        break

            # Убираем дубликаты и текущее направление, если оно было
            possible_directions = list(set(possible_directions))
            if self.direction in possible_directions:
                possible_directions.remove(self.direction)

            # Если нет других возможных направлений, кроме текущего, или это тупик,
            # то продолжаем двигаться в текущем направлении или разворачиваемся.
            if not possible_directions:
                 self.direction = (self.direction + 2) % 4 # Разворот
            else:
                self.direction = random.choice(possible_directions)


    def move(self, intersections, streets):
        self.update_direction(intersections, streets)

        # Обновление позиции в зависимости от направления и скорости
        if self.direction == 0:  # Вправо
            self.x += self.speed
        elif self.direction == 1: # Вверх
            self.y += self.speed
        elif self.direction == 2: # Влево
            self.x -= self.speed
        elif self.direction == 3: # Вниз
            self.y -= self.speed

        # Убедимся, что люди не выходят за границы города
        self.x = np.clip(self.x, 0, TOTAL_CITY_DIM)
        self.y = np.clip(self.y, 0, TOTAL_CITY_DIM)


