def calc_size(tri):
    """
    вычислить размер одного треугольника
    """

    a = length(tri[0], tri[1], tri[2], tri[3])
    b = length(tri[4], tri[5], tri[2], tri[3])
    c = length(tri[4], tri[5], tri[0], tri[1])

    p = (a + b + c) / 2

    s = sqrt( p * (p-a) * (p-b) * (p-c) )

    return s


def length(x1, y1, x2, y2):
    """
    вычислить длину стороны
    """

    size = sqrt( (x1-x2) ** 2 + (y1-y2) ** 2 )

    return size
    

# ----------------------------------------------
# run all

from itertools import combinations

def main():
    """
    запустить всё
    """

    points = make_tri()
    print(f"{points=}")

    sq = 0.
    btri = []
    for tri in combinations(points, 3):
        print("треугольник:\n", tri)
        trix = ( tri[0][0], tri[0][1], tri[1][0], tri[1][1], tri[2][0], tri[2][1])
        print(trix)
        cs = calc_size(trix)
        # ~ cs = 0.
        if cs > sq:
            sq = cs
            btri = tri
            print("нашли получше!")
        print("эта площадь:  ", cs)
        print("макс. площадь:", sq)        
        
    print("\nмаксимальный треугольник с площадью:", sq)
    print(btri)
    print()

main()

