# ~ Даны координаты несколькитх (3+) точек на плоскости.
# ~ Найти треугольнки с наибольшей площадью
# ~ (если несколько, то любой).

# ~ Решение:
# ~ 1. по itertools.combinations перебираем все возможные треугольники
# ~ 2. по кооринатам находим длины сторон, как гипотенузы по теореме Пифагора,
# ~ 3. затем периметр, полупериметр,
# ~ 4. и по формуле Герона находим площадь,
# ~ 5. затем выбираем наибольшую.

LIMIT = 4    # сколько всего точек делать

from math import sqrt
from pprint import pp
from random import random as rr

SCALE = 10.      # разбор данных (расстояние от нуля)

# ----------------------------------------------
# make random triangles

def rc():
    """
    дать случайную координату
    """

    return rr()*2*SCALE - SCALE
    

def make_tri():
    """
    сделать треугольники
    """

    trirandom = []
    
    for i in range(LIMIT):
        trirandom.append( (rc(), rc()) )

    return trirandom
    

