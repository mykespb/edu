# ~ Телевизионные башни (телебашни) оцениваются по комплексу инженерных, архитектурных и технологических параметров. Ниже приведены ключевые характеристики, которые используются при их проектировании и описании:
# ~ ## Геометрические и конструктивные характеристики

# ~ * Высота (Height): Полная высота конструкции (включая шпиль и передающие антенны) и высота самой бетонной или стальной корзины башни.
# ~ * Материал (Material): Тип несущей конструкции — монолитный железобетон, стальная решетчатая структура (как Шуховская или Эйфелева башни) или комбинированный тип.
# ~ * Глубина и тип фундамента (Foundation): Масса и тип основания (плитный, свайный), обеспечивающие устойчивость при наклоне и смещении центра тяжести.
# ~ * Амплитуда колебаний вершины (Oscillation amplitude): Допустимое отклонение шпиля башни при ураганном ветре (измеряется в метрах).

# ~ ## Технологические и радиопередающие параметры

# ~ * Зона покрытия / Радиус вещания (Coverage radius): Расстояние прямой видимости, на которое транслируется сигнал (напрямую зависит от высоты башни и рельефа местности).
# ~ * Количество и типы антенн (Antennas): Передающие устройства для цифрового (DVB-T2) и аналогового ТВ, FM-радиостанций, сотовой, спутниковой и специальной связи.
# ~ * Мощность передатчиков (Transmitter power): Суммарная излучаемая мощность оборудования (в киловаттах).

# ~ ## Инженерно-эксплуатационные системы

# ~ * Наличие смотровых площадок (Observation decks): Высота их расположения, а также деление на закрытые (застекленные) и открытые.
# ~ * Скорость и тип лифтов (Elevator speed): Наличие скоростных высотных лифтов и время подъема до верхних ярусов (в метрах в секунду).
# ~ * Система гашения колебаний (Dampers): Наличие и тип маятниковых или водяных демпферов на верхних этажах для гашения раскачивания от ветра.
# ~ * Ветровая и сейсмическая устойчивость (Wind & Seismic resistance): Максимальная скорость ветра (в м/с) и балл сейсмической активности (по шкале Рихтера), которые способна выдержать башня.

# ~ да, в формате list[dict], ключи короткие, английские, но дай список с объяснениями, что эти ключи значат; дай по 30 башням мира, указав
# ~ - n: номер,
# ~ - name: название,
# ~ - city: город,
# ~ - country: страна,
# ~ а также все упомянутые выше характеристики.

## Расшифровка коротких английских ключей (Словарь полей)
# ~ Каждый объект в списке содержит структурированные технические и архитектурные параметры башни:

# ~ * n (int): Порядковый номер башни в данном списке.
# ~ * name (str): Официальное название сооружения на русском языке.
# ~ * city (str): Город расположения башни.
# ~ * country (str): Страна расположения башни.
# ~ * h (int/float): Полная высота конструкции вместе со шпилем и антеннами (в метрах).
# ~ * mat (str): Основной конструкционный материал (железобетон, сталь, комбинированный).
# ~ * fnd (str): Тип фундамента и его глубина / особенности заложения.
# ~ * osc (float): Максимальная расчетная амплитуда колебаний вершины при ураганном ветре (в метрах).
# ~ * rad (int): Средний эффективный радиус вещания / зона покрытия сигнала (в километрах).
# ~ * ant (int): Примерное количество или тип поддерживаемых частотных диапазонов / антенных комплексов (ТВ, РВ, сотовая связь).
# ~ * pwr (int): Суммарная излучаемая мощность основных передатчиков (в киловаттах, кВт).
# ~ * obs (int): Высота расположения главной смотровой площадки (в метрах, 0 — если закрыта для публики).
# ~ * elv (float): Максимальная скорость движения пассажирских высотных лифтов (в метрах в секунду, м/с).
# ~ * dmp (str): Тип системы стабилизации и гашения колебаний (маятниковый демпфер, водяной, пассивный за счет формы).
# ~ * res (str): Максимальная устойчивость конструкции: скорость ветра (в м/с) и сейсмика (в баллах).

