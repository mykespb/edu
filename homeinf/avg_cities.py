# ~ Ниже представлен глобальный срез статистических данных по ключевым мировым мегаполисам и запрошенным городам. Значения долей от зарплаты (avg_home, avg_meals, avg_trans) рассчитаны как отношение средних расходов на человека (аренда 1-комнатной квартиры, базовая продуктовая корзина, единый проездной) к официальной средней чистой зарплате (net) в данном городе на середину 2026 года.
# ~ 💡 Пояснения к статистике расходовЖилье (avg_home): Включает долгосрочную аренду жилья (Rent) в черте города и коммунальные услуги (Utilities). В мегаполисах уровня Нью-Йорка, Лондона и Торонто этот показатель в последние годы стабильно превышает психологическую отметку в 45-50% от среднего локального дохода.Еда (avg_meals): Подразумевает базовую закупку продуктов в супермаркетах для домашнего приготовления (Groceries), рассчитанную на взрослого человека, без регулярного посещения ресторанов премиум-класса.Транспорт (avg_trans): Расчет выполнен по стоимости безлимитного месячного проездного билета на метро и наземные виды общественного транспорта в пределах основных зон города. Расходы на личные автомобили (бензин, страховка, паркинг) сюда не включены.

