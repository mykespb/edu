# ~ что как подорожало?

def compare(table, title):
    print(f"""
***************************************
*** работаем с таблицей: {title:10} ***
***************************************
""")

    ymin, ymax = table[-1][0], table[1][0]

    print(f"""за годы с {ymin} по {ymax}
    """)
    
    over = [ table[-1][rooms] /  table[1][rooms] for rooms in range(1, 4) ]
    comps = zip(table[0][1:], over)
    scomps = sorted(comps, key = lambda x: x[1], reverse=True)

    print(f"""данные по квартирам:
{list((table[0][i], o) for i, o in enumerate(over, start=1))}

максимальное значение: {scomps[0]}
среднее значение: {sum( [x[1] for x in scomps] ) / len(scomps)}
    """)

