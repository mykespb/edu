n_neighbors_range = range(1, 15) # От 1 до 14 соседей

accuracies = []

for n in n_neighbors_range:
    # Инициализируем модель KNeighborsClassifier с текущим n_neighbors
    knn = KNeighborsClassifier(n_neighbors=n)

    # Обучаем модель на обучающих данных
    knn.fit(X_train, y_train)

    # Делаем предсказания на тестовых данных
    y_pred = knn.predict(X_test)

    # Оцениваем точность модели
    accuracy = accuracy_score(y_test, y_pred)
    accuracies.append(accuracy)

    print(f"n_neighbors: {n}, Точность: {accuracy:.2f}")

# Находим лучшее значение n_neighbors и соответствующую точность
best_n_neighbors = n_neighbors_range[accuracies.index(max(accuracies))]
best_accuracy = max(accuracies)

print(f"\nОптимальное количество соседей (n_neighbors): {best_n_neighbors}")
print(f"Лучшая точность: {best_accuracy:.2f}")

