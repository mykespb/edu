# ~ ## Что здесь принципиально изменилось?

# ~ * scaler.fit_transform(X): Этот метод делает две вещи сразу. Сначала fit вычисляет среднее значение и стандартное отклонение для каждого столбца в X. Затем transform преобразует все числа. Теперь длина хвоста 40 и размер 2 имеют одинаковый «вес» для модели.
# ~ * scaler.transform([new_animal]): Новые данные для предсказания обязательно нужно пропустить через тот же самый трансформер. Если этого не сделать, модель попытается сравнить обычные сырые числа (например, длину хвоста 2) с предобработанными мини-числами в своей базе данных, и ответ будет совершенно случайным. Обратите внимание, что здесь используется только transform (без fit), так как параметры масштабирования должны браться исключительно из обучающей выборки.

