# ~ Суть алгоритма K-ближайших соседей (KNN) заключается в простом правиле: «Скажи мне, кто твой друг, и я скажу, кто ты».
# ~ Алгоритм не строит сложных математических формул и не ищет скрытые закономерности. Он просто запоминает все обучающие примеры и сравнивает новые объекты с уже известными.
# ~ ------------------------------
# ~ ## Как работает алгоритм (по шагам)

# ~ [ Обучающая выборка ] ──> [ Появление нового объекта ] ──> [ Расчет расстояний ] ──> [ Голосование соседей ]


   # ~ 1. Запоминание: Модель сохраняет координаты всех точек из обучающего набора данных.
   # ~ 2. Расчет расстояния: Когда появляется новый (неизвестный) объект, алгоритм считает расстояние от него до каждой точки из своей базы данных.
   # ~ 3. Выбор соседей: Модель отбирает $K$ самых близких точек (где $K$ — это число, которое вы задаете сами).
   # ~ 4. Голосование:
   # ~ * В задаче классификации (как с животными) новый объект получает тот класс, который чаще всего встречается у выбранных соседей.
      # ~ * В задаче регрессии (предсказание чисел, например, цены квартиры) алгоритм берет среднее арифметическое значение среди соседей.
   
# ~ ------------------------------
# ~ ## Пример «на пальцах»
# ~ Представьте, что мы выбираем значение $K = 3$ (ищем трех ближайших соседей):

# ~ * Появилось неизвестное животное.
# ~ * Алгоритм посчитал расстояния и нашел 3 самые похожие на него точки в базе.
# ~ * Среди них оказалось: 2 кошки и 1 собака.
# ~ * Большинство побеждает (2 против 1) — модель классифицирует объект как кошку.

# ~ ------------------------------
# ~ ## На что влияет параметр K?

# ~ * Если $K = 1$: Модель смотрит только на одного единственного «близнеца». Это делает ее очень чувствительной к шуму и ошибкам в данных. Если в базу случайно попала «тихая лающая собака», модель может перепутать ее с хомяком.
# ~ * Если $K$ слишком большое: Модель станет слишком обобщать. Например, если в базе 100 собак и 3 хомяка, то при $K=50$ модель почти всегда будет предсказывать «собаку», просто потому что их физически больше вокруг.

# ~ ------------------------------
# ~ ## Главные плюсы и минусы
# ~ Плюсы:

# ~ * Простота: Алгоритм интуитивно понятен и легко пишется вручную.
# ~ * Универсальность: Подходит и для классификации, и для предсказания чисел.
# ~ * Нет этапа долгого обучения: Метод fit() просто сохраняет данные в память.

# ~ Минусы:

# ~ * Медленная работа на больших данных: При каждом предсказании нужно пересчитывать расстояние до всех точек в базе. Если у вас миллион строк, компьютер зависнет.
# ~ * Проклятие размерности: Если у объекта слишком много характеристик (например, 100 признаков), расстояния между всеми точками становятся примерно одинаковыми, и алгоритм перестает работать.

