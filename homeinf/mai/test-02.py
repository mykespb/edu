from sklearn.neighbors import KNeighborsClassifier

# Названия животных (целевая переменная)
types = ["Хомяк", "Кошка", "Собака"]

# Признаки (Характеристики)
size = [0, 1, 2]             # 0 - маленькое, 1 - среднее, 2 - большое
noize_level = [0, 1, 2]      # 0 - тихое, 1 - не самое тихое, 2 - громкое
tail_length = [1, 25, 40]    # Длина хвоста в сантиметрах
fur_amount = [0, 1, 2]       # 0 - лысое, 1 - короткошерстное, 2 - пушистое

# Собираем все признаки в одну матрицу X
X = [
    [size[i], noize_level[i], tail_length[i], fur_amount[i]] 
    for i in range(len(types))
]
y = types

X.append([0,2,1,2])
y.append("Кошка")
X.append([0,2,10,2])
y.append("Кошка")

# Создаем и обучаем модель (используем 1 соседа)
# ~ model = KNeighborsClassifier(n_neighbors=1)

# Создаем и обучаем модель (используем 2 соседей)
model = KNeighborsClassifier(n_neighbors=2)

model.fit(X, y)

# Характеристики нового (неизвестного) животного для проверки
new_animal = [
    0,   # size: маленькое
    1,   # noize_level: не самое тихое
    2,   # tail_length: хвост 2 см (очень короткий)
    1    # fur_amount: короткошерстное
]

# Предсказание
prediction = model.predict([new_animal])
print(f"Предсказание типа животного: {prediction[0]}")



