# ~ Вот обновленный код. Теперь в обучающей выборке у нас 7 животных: 1 хомяк, 3 разные кошки (обычная, мейн-кун, сфинкс) и 3 разные собаки (чихуахуа, овчарка, пудель).
# ~ Чтобы модель могла адекватно сопоставлять эти разные варианты, мы увеличили количество соседей до n_neighbors=3. Теперь решение принимается большинством голосов трех самых похожих животных.
## Обновленный код с расширенной выборкой

from sklearn.neighbors import KNeighborsClassifier
from sklearn.preprocessing import StandardScaler

# Расширенная база данных (7 животных)# Признаки: [размер (0-2), шум (0-2), хвост (см), шерсть (0-2)]

types = ["Хомяк", "Кошка", "Собака"]

# ~ X = [   # Хомяк,  # Кошка 1 (Обычная),  # Кошка 2 (Большой пушистый Мейн-кун),  # Кошка 3 (Лысый Сфинкс),   # Собака 1 (Маленькая громкая Чихуахуа),  # Собака 2 (Большая Немецкая овчарка)
    # ~ [1, 1, 10, 2]   # Собака 3 (Средний кудрявый Пудель)
# ~ ]
# Соответствующие им правильные ответы

# Собираем все признаки в одну матрицу X
X = [
    [size[i], noize_level[i], tail_length[i], fur_amount[i]] 
    for i in range(len(types))
]
y = types

X.append([0,2,1,2])
y.append("Кошка")
X.append([0,2,10,2])
y.append("Кошка")
X.append([1, 1, 10, 2])
y.append("Собака")

y = [
    "Хомяк",
    "Кошка", "Кошка", "Кошка",
    "Собака", "Собака", "Собака"
]

# Шаг 1: Масштабирование признаков

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Шаг 2: Создание модели с 3 соседями (K=3)

model = KNeighborsClassifier(n_neighbors=3)
model.fit(X_scaled, y)

# Тест 1: Проверяем на новом «непонятном» животном# Параметры: средний размер (1), не самое тихое (1), хвост 15 см, пушистое (2)# Это описание похоже и на Мейн-куна, и на Пуделя.

new_animal_1 = [[1, 1, 15, 2]]
new_animal_1_scaled = scaler.transform(new_animal_1)
prediction_1 = model.predict(new_animal_1_scaled)

# Можно также посмотреть вероятность для каждого класса

probabilities = model.predict_proba(new_animal_1_scaled)

print(f"Тест 1. Предсказание типа: {prediction_1[0]}")
print(f"Вероятности классов {model.classes_}: {probabilities[0]}")

print("-" * 40)

# Тест 2: Проверяем маленькое, но очень громкое животное с коротким хвостом# Параметры: маленькое (0), громкое (2), хвост 4 см, короткошерстное (1)

new_animal_2 = [[0, 2, 4, 1]]
new_animal_2_scaled = scaler.transform(new_animal_2)
prediction_2 = model.predict(new_animal_2_scaled)

print(f"Тест 2. Предсказание типа: {prediction_2[0]}")

## Как теперь работает модель с разнообразием

   # ~ 1. Голосование соседей (n_neighbors=3): Если мы подадим на вход параметры [1, 1, 15, 2] (Тест 1), алгоритм найдет трех самых близких соседей в нормализованном пространстве. Скорее всего, это будут Кошка 1, Кошка 2 и Собака 3 (Пудель). Так как кошек среди соседей большинство (2 против 1), модель выберет ответ Кошка.
   # ~ 2. Метод predict_proba: Показывает уверенность модели в процентах. Если среди 3 соседей оказались 2 кошки и 1 собака, то вероятность для кошки составит $66.6\%$ ($2/3$), а для собаки — $33.3\%$ ($1/3$).
   # ~ 3. Обработка исключений: В Тесте 2 мы передали маленькое, но громкое животное. Из-за высокого уровня шума (2) алгоритм поймет, что это точно не хомяк, отбросит его и выберет Собаку (сработает сходство с Чихуахуа).

