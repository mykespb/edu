def randmat(size: int = 3, limit: int = 100) -> list[list[int]]:
    """
    Генерирует случайную целочисленную матрицу.
    Предел: limit.
    Размер: size.
    """

    return [ [ randint(1, limit) for col in range(size)] for row in range(size)]


