def compare(q1: list[list[int]], q2: list[list[int]]) -> bool:
    """
    сравнить 2 матрицы на поворотность
    """

    if q1 == q2:
        return True

    q2a = rotmat(q2)
    if q1 == q2a:
        return True
    
    q2b = rotmat(q2a)
    if q1 == q2b:
        return True
    
    q2c = rotmat(q2b)
    if q1 == q2c:
        return True

    return False


