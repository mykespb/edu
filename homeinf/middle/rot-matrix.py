def rotmat(matrix: list[list[int]]) -> list[list[int]]:
    """
    Принимает квадратную матрицу и возвращает новую матрицу,
    повернутую на 90 градусов по часовой стрелке.
    """
    # zip(*matrix) группирует элементы по столбцам, а [::-1] разворачивает строки
    return [list(row[::-1]) for row in zip(*matrix)]


def hormir(matrix: list[list[int]]) -> list[list[int]]:
    """
    Принимает квадратную матрицу и возвращает новую матрицу,
    отражённую относительно горизонтальной средней линии.
    """
    return matrix[::-1]
    
