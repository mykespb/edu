# ~ Дан 4-мерный куб со стороной 4 из целых чисел.
# ~ Сколько из них делятся на 4?

from itertools import product
from random import randint
from pprint import pp, pprint

def make():
    return [[[[ randint(10, 99) for _ in range(4)]
        for _ in range(4)]
            for _ in range(4)]
                for _ in range(4)]
    

def main():
    k = make()
    pp(k)

    div4 = 0
    for i1, i2, i3, i4 in product(range(4), range(4), range(4), range(4)):
        div4 += (k[i1][i2][i3][i4] % 4 == 0)

    print(f"кратны 4: {div4} из {4*4*4*4}")

main()


