# перворачиваем натуральное число
# (ведущие нули не выводить)

def revnum1(n):
    """обработать"""

    return str(n)[::-1]

def revnum2(n):
    """обработать"""

    return int(str(n)[::-1])

def test(n):
    """протестировать одно число"""
    
    print("было:", n, ", стало", revnum2(n))


test(0)
test(1)
test(12)
test(123456789)

