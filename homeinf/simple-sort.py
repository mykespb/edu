import random

def sorter(arr: list) -> list:
    if not arr:
        return []

    out = arr[:]
    lenout = len(out)
    for i in range(lenout-1):
        for j in range(i+1, lenout):
            if out[i] > out[j]:
                out[i], out[j] = out[j], out[i]

    return out


def test(times: int =1) -> None:
    for rept in range(times):
        arr = [random.randint(-99, 99) for _ in range(10)]
        print(f"{rept}: {arr} => {sorter(arr)}")


test(10)


