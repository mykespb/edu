# ~ Версия с 2 списками.

import math

# определение функции
def primes(limit=100):
    """печать простых до limit"""

    ready = [2]
    todo = [ x for x in range(3, limit) ]

    # ~ print(f"{ready=}")
    # ~ print(f"{todo=}")

    while todo:
        elem = todo.pop(0)
        ready.append(elem)
        todo = [ x for x in todo if x % elem ]

        # ~ print(f"{ready=}")
        # ~ print(f"{todo=}")

    return ready


# вызов функции
