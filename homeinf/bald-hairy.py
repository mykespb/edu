bald  = "Горбачёв Путин_1 Львов Александр_3 Андропов Ленин Николай_1 Хрущёв Путин_2"
hairy = "Николай_2 Керенский Медведев Ельцин Сталин Александр_2 Черненко Брежнев"
order = "Николай_1 Александр_2 Александр_3 Николай_2 Львов Керенский Ленин Сталин Хрущёв Брежнев Андропов Черненко Горбачёв Ельцин Путин_1 Медведев Путин_2"

# ~ print(f"{bald=}")
