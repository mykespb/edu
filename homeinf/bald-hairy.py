# --- solution ---

# ~ persons = bald.split(), hairy.split()
# ~ "лысый" if name in bald.split() else "волосатый")

persons = bald, hairy

for num, name in enumerate(order.split(), 1):
    print(num, "-", name, "-",
        "лысый" if name in bald else "волосатый")

print("\nрезультат: ", 
    "правда" if all( [ ruler in persons[num % 2] for num, ruler in enumerate(order.split()) ] ) else "неправда",
    "\n")

