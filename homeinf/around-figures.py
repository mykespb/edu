from pprint import pprint
from random import randint

# размер поля, ширина и высота
SIZE = 30

# откуда докуда заполняем
FROM = SIZE // 4
UPTO = SIZE * 3 // 4


def make(nums = -1):
    """заполнить поле случайно.
    nums > 0: поставить столько точек,
    nums < 0: заполнить столько процентов площади
    """

    global board

    board = [ [ 0 for j in range(SIZE) ] for i in range(SIZE) ] 

    if nums < 0:
        nums = randint(SIZE * SIZE * abs(nums) // 100)

    for i in range(nums):
        board[randint(FROM, UPTO)][randint(FROM, UPTO)] = 1

    pprint(board, compact = True, width = 100)


def detect():
    """обнаружить точки и обвести"""

    global board

    left = top = SIZE
    right = bottom = -1

    for i in range(SIZE):
        for j in range(SIZE):
            if board[i][j]:
                left = min(left, j)
                right=  max(right, j)
                top = min(top, i)
                bottom = max(bottom, i)

    print(f"найдены координаты точек: {top=}, {bottom=}, {left=}, {right=} ")

    for j in range(left-1, right+1):
        board[top-1][j] = board[bottom+1][j] = 2

    for i in range(top-1, bottom+1):
        board[i][left-1] = board[i][right+1] = 2

    pprint(board, compact = True, width = 100)


make(10)
detect()


