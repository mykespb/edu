from random import randint as ri

MIN = 1
MAX = 4
SIZE = 10

pattern = list(range(MIN, MAX+1))
print("pattern", pattern)


def gen():
    """генерируем случайный квадрат и возвращаем"""
    return [[ri(MIN, MAX) for i in range(SIZE)] for j in range(SIZE)]


def calc(k):
    """считаем квадратики"""
    num = 0
    for i in range(SIZE-1):
        for j in range(SIZE-1):
            if sorted([k[i][j], k[i][j+1], k[i+1][j], k[i+1][j+1] ]) == pattern:
                num += 1
                print(f"{num=}, {i=}, {j=}")

    print(f"\ntotal: {num}")
    

def show(k):
    """показываем квадрат"""
    print()
    for i in range(SIZE):
        for j in range(SIZE):
            print("%2d" % (k[i][j],), end="")
        print()
    print()
    

k = gen()
show(k)
calc(k)


