def cprint(kv, wleft=20):
    for k, v in kv:
        print(f"{k:{wleft}} | {v}")


from collections import Counter
from pprint import pprint

# ------------------------ tests -------------------------

all_films = films['official_films'] + films['unofficial_films']

# -------------------------------------------------
part("1. какие актёры играли Бонда, упорядочить по году первого фильма")

actors = []
data = []

for film in all_films:
    if film['actor'] not in actors:
        actors.append(film['actor'])
        data.append((film['year'], film['actor']))

cprint(data, 10)

# -------------------------------------------------
part("2. какие актёры играли Бонда, кто сыграл больше всех?")

cnt = Counter(
    [
        film['actor']
        for film in all_films
    ]
    )

pprint(dict(cnt))

# -------------------------------------------------
part("3. какие актёры играли Бонда, кто играл дольше всех?")

actors = {}

for film in all_films:
    actor = film['actor']
    year  = film['year']
    if actor in actors:
        if actors[actor]['first'] > year:
            actors[actor]['first'] = year
        if actors[actor]['last'] < year:
            actors[actor]['last'] = year
        actors[actor]['long'] = actors[actor]['last'] - actors[actor]['first'] + 1
    else:
        actors[actor] = {
            'first': year,
            'last': year,
            'long': 1
            }

# ~ pprint(actors)

long_actors = [
    (k, *(v.values()))
    for k, v in actors.items()
    ]

# ~ print('долгейшие:')

long_actors.sort(key = lambda x: x[-1], reverse=True)

longest_name = max( [len(actor[0]) for actor in long_actors] )
# ~ print(longest_name)

for actor, first, last, long in long_actors:
    print(f"{actor:{longest_name}} играл с {first} до {last} в течение {long} {'года' if long==1 else 'лет'}")

# ------------------------ end -------------------------

part('end')

