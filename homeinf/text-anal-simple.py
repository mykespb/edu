# 1. Извлечение признаков (Паспорт стиля)
def get_style_vector(text):
    lines = [line.strip() for line in text.split('\n') if line.strip()]
    words = re.findall(r'\b\w+\b', text.lower())
    
    if not words or not lines:
        return [0, 0, 0, 0, 0, 0]
        
    # Метрика 1: Богатство словаря (TTR)
    ttr = len(set(words)) / len(words)
    
    # Метрика 2: Средняя длина слова
    avg_word_len = sum(len(w) for w in words) / len(words)
    
    # Метрика 3: Среднее количество слов в строке
    avg_line_len = len(words) / len(lines)
    
    # Метрика 4 и 5: Частота ключевых служебных слов (доля от всех слов)
    # Возьмем самые бессознательные маркеры: союзы "и", "а" и предлог "в"
    count_i = words.count('и') / len(words)
    count_v = words.count('в') / len(words)
    count_a = words.count('а') / len(words)
    
    # Веса (коэффициенты), чтобы сбалансировать масштаб метрик
    # Умножаем маленькие доли, чтобы они имели равный вес с длинами слов/строк
    return [
        ttr * 10, 
        avg_word_len, 
        avg_line_len, 
        count_i * 100, 
        count_v * 100, 
        count_a * 100
    ]

# 2. Функция вычисления сходства (Евклидово расстояние)
def calculate_distance(vector1, vector2):
    return math.sqrt(sum((a - b) ** 2 for a, b in zip(vector1, vector2)))

# 3. Главная функция-классификатор
def predict_author(unknown_text, authors_profiles):
    """
    unknown_text: str - текст неизвестного стиха
    authors_profiles: dict - словарь вида {'Имя Автора': [вектор_признаков]}
    """
    unknown_vector = get_style_vector(unknown_text)
    
    best_match = None
    min_distance = float('inf')
    
    print("--- Результаты анализа ---")
    for author, profile_vector in authors_profiles.items():
        distance = calculate_distance(unknown_vector, profile_vector)
        print(f"Расстояние до стиля '{author}': {distance:.3f}")
        
        if distance < min_distance:
            min_distance = distance
            best_match = author
            
    print("--------------------------")
    return best_match



