from collections import Counter
import math
import re

def calculate_ttr(text):
    words = re.findall(r'\b\w+\b', text.lower())
    if not words: return 0
    return len(set(words)) / len(words)

def average_lengths(text):
    lines = [line.strip() for line in text.split('\n') if line.strip()]
    words = re.findall(r'\b\w+\b', text.lower())
    
    avg_word_len = sum(len(w) for w in words) / len(words) if words else 0
    avg_line_len = len(words) / len(lines) if lines else 0
    
    return avg_word_len, avg_line_len

def top_function_words(text, top_n=10):
    # Список частых служебных слов для фокуса
    stopwords = {'и', 'в', 'во', 'не', 'на', 'что', 'с', 'со', 'как', 'а', 'я', 'он', 'ты'}
    words = re.findall(r'\b\w+\b', text.lower())
    filtered_words = [w for w in words if w in stopwords]
    
    counts = Counter(filtered_words)
    # Переводим в проценты от общего числа слов, чтобы длина текста не влияла
    total_words = len(words) if words else 1
    return {word: count / total_words for word, count in counts.most_common(top_n)}

def punctuation_density(text):
    lines = [l for l in text.split('\n') if l.strip()]
    if not lines: return 0, 0
    
    exclamations = text.count('!') / len(lines)
    questions = text.count('?') / len(lines)
    return exclamations, questions

